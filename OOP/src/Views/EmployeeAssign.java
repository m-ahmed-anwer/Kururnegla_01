    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        int x= JOptionPane.showConfirmDialog(rootPane, "Do you want to Assign the Order", "Message", HEIGHT);
        if(x==0){
            int orderId=Integer.parseInt(jTextField1.getText());
            int empId=Integer.parseInt(jTextField2.getText());
            
            try {
                String query="Update orders set EmployeeId ='"+empId+"' where OrderId ='"+orderId+"' ";
                pst = con.prepareStatement(query);
                pst.execute();
                tableAssign();
                jTextField1.setText("");
                jTextField2.setText("");
                

            } catch (Exception e) {
                System.out.println(e);
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

